// while

let index = 0 // initialization
while(index <= 10){ // condition
    console.log(`value of index is: ${index}`); // code block / statement
    index= index + 2 // updation
}

let myArray=['flash','batman',"superman"]
let arr=0
while(arr<myArray.length){ // condition
    console.log('value is: ',myArray[arr]); // code block / statement
    arr =arr+1 // updation
}

let scose=11 // initialization
do{ // condition    
    console.log('score is: ',scose); // code block / statement
    scose++ // updation
}while(scose<=10); // condition check // at least one time the code block will be executed